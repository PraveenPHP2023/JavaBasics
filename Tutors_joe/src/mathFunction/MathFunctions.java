package mathFunction;

public class MathFunctions {

	public static void main(String[] args) {

		// MathFunction in java
		
		// Absolute positive value
		System.out.println("absolute positive: "+Math.abs(-45));
		
		//  Round
		System.out.println("Round: "+Math.round(2.5));
		System.out.println("Round: "+Math.round(2.22));
		
		// ceil
		System.out.println("Ceil: "+Math.ceil(2.2));
		
		// Floor
		System.out.println("Floor: "+Math.floor(2.86));
		
		double a =2;
		double b = 3;
		
		// Max
		System.out.println("Maximum number of a and b: "+Math.max(a, b));
		
		// Square root
		System.out.println("Square root of b: "+Math.sqrt(b));
		
		// Power
		System.out.println("power of a: "+Math.pow(a, 3));
		
		System.out.println("Log of a: "+Math.log(a));
		
		System.out.println("Log10 of a: "+Math.log10(a));
		
		System.out.println("Sin value of a: "+Math.sin(a));
	}

}
