package loop;

import java.util.Arrays;
import java.util.Scanner;

public class For_each {

	public static void main(String[] args) {
		// For each loop or Enhanced for loop
		
		int numbers [] = {10,20,30,50,40,60,70};
		
		for(int n : numbers)
		{
			System.out.println(n);
			
		}

	}

}
